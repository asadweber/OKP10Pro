
var baseurl = $('#txtBaseUrl').val();

$(document).ready(function () {

    AddAntiForgeryToken = function (data) {
        data.__RequestVerificationToken = $('input[name=__RequestVerificationToken]').val();
        return data;
    };

    $('body').on('click', '#btnWorkFlowColorCodeUpdate', function (event) {
        try {
            event.preventDefault();
            var form = $('#frmUpdateWorkFlowColorCode');
            jQuery.validator.unobtrusive.parse();
            jQuery.validator.unobtrusive.parse(form);

            if (form.valid()) {

                var input = AddAntiForgeryToken({
                    token: $(".txtUserSTK").val(),
                    userinfo: $(".txtServerUtilObj").val(),
                    useripaddress: $(".txtuserip").val(),
                    sessionid: $(".txtUserSes").val(),
                    methodname: "WorkFlowColorCodeUpdate",
                    currenturl: window.location.href,
                    
                                        colorcodeid : $('#colorcodeid').val(),
processid : $('#processid').val(),
templetid : $('#templetid').val(),
templetdetlid : $('#templetdetlid').val(),
levelid : $('#levelid').val(),
statusid : $('#statusid').val(),
colorcode : $('#colorcode').val(),
comments : $('#comments').val(),
remarks : $('#remarks').val() 
                });

                confirmationDialog(_getCookieForLanguage("_confirmationTitle"), _getCookieForLanguage("_updateConfirmation"), _getCookieForLanguage("_btnYes"), _getCookieForLanguage("_btnNo")).then(function (answer) {
                    if (answer == "true") {
                        $.ajax({
                            url: "WorkFlowColorCodeUpdate",
                            data: input,
                            type: 'POST',
                            success: function (data) {

                                if (data.status === "success") {
                                    inforamtionDialog(data.title, data.responsetext, _getCookieForLanguage("_btnOK")).then(function (answer) {
                                        $("#WorkFlowColorCodeDt").DataTable().ajax.reload();
                                        $('#modal-container-WorkFlowColorCodeedit').modal('hide');
                                    });
                                }
                                else {
                                    inforamtionDialog(data.title, data.responsetext, _getCookieForLanguage("_btnOK")).then(function (answer) {
                                        if (answer == "true") {
                                            $("#WorkFlowColorCodeDt").DataTable().ajax.reload();
                                            $('#modal-container-WorkFlowColorCodeedit').modal('hide');
                                        }
                                    });
                                }
                            }
                        });
                    }
                });
            }
            else {
                return;
            }
        } catch (e) {
            $.alert({
                title: _getCookieForLanguage("_informationTitle"),
                content: e.message,
                type: 'red'
            });
        }
    });


    $('body').on('click', '#btnWorkFlowColorCodeReturn', function (event) {
        try {
            event.preventDefault();
            window.location.href = baseurl + "WorkFlowColorCode/WorkFlowColorCode";
        } catch (e) {
            $.alert({
                title: _getCookieForLanguage("_informationTitle"),
                content: e.message,
                type: 'red'
            });
        }
    });
});

	