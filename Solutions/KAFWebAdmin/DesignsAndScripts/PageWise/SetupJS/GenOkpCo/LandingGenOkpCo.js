function GetDataHrBasicProfile(val) {
    try {
        AddAntiForgeryToken = function (data) {
            data.__RequestVerificationToken = $('input[name=__RequestVerificationToken]').val();
            return data;
        };

        var input = AddAntiForgeryToken({
            token: $(".txtUserSTK").val(),
            userinfo: $(".txtServerUtilObj").val(),
            useripaddress: $(".txtuserip").val(),
            sessionid: $(".txtUserSes").val(),
            methodname: "HrPersonalInfoTableData",
            currenturl: window.location.href,
            hrbasicid: val
        });
        $.ajax({
            url: baseurl + "GenOkpCo/GetAllHrBasicProfileData",
            type: 'POST',
            data: input,
            success: function (response) {
                var data = response.data;
                if (data != "") {
                    $('#hr_svcinfo_militarynokw').val(data[0].militarynokw);
                    $('#hr_svcinfo_militarynobd').val(data[0].militarynobd);
                    $('#hr_svcinfo_civilid').val(data[0].civilid);
                    $('#hr_svcinfo_passportno').val(data[0].passportno);
                    $('#hr_svcinfo_fullname').val(data[0].fullname);
                    $('#hrbasicid').val(data[0].hrbasicid);
                }
                else {
                    $.alert({
                        title: _getCookieForLanguage("_informationTitle"),
                        content: "Data not found",
                        type: 'red'
                    });
                }
            }
        });
    } catch (e) {
        $.alert({
            title: _getCookieForLanguage("_informationTitle"),
            content: e,
            type: 'red'
        });
    }
}
function GetAllDataGenOkpCo() {
    try {
        AddAntiForgeryToken = function (data) {
            data.__RequestVerificationToken = $('input[name=__RequestVerificationToken]').val();
            return data;
        };

        var input = AddAntiForgeryToken({
            token: $(".txtUserSTK").val(),
            userinfo: $(".txtServerUtilObj").val(),
            useripaddress: $(".txtuserip").val(),
            sessionid: $(".txtUserSes").val(),
            methodname: "GenOkpCoTableData",
            currenturl: window.location.href
        });
        
        $('#GenOkpCoDt').DataTable({
            "destroy": true,
            "bFilter": true,
            "columnDefs": [{
                "targets": 0,
                "searchable": true,
                "orderable": true
            }],
            "language":
            {
                "processing": "<div class='overlay custom-loader-background'><i class='fa fa-cog fa-spin custom-loader-color'></i></div>"
            },
            "processing": true,
            "serverSide": true,
            "autoWidth": false,
            "responsive": true,
            "ajax": {
                "url": baseurl + "GenOkpCo/GenOkpCoTableData",
                "type": "POST",
                "data": input
            },
            error: function (jqXHR, textStatus, errorThrown) {
                $.alert({
                    title: _getCookieForLanguage("_informationTitle"),
                    content: jqXHR.responseJSON.Error,
                    type: 'red'
                });
            },
            "columns": [
            
                { "data": "okpName", "searchable": true, "sortable": true, "orderable": true },                {
                    "data": "fromdate",
                    "render": function (data, type, full, meta) {
                        return datetoStr(data);
                    }
                },                {
                    "data": "todate",
                    "render": function (data, type, full, meta) {
                        return datetoStr(data);
                    }
                },                {
                    "data": "ex_nvarchar1",
                    "render": function (data, type, full, meta) {
                        return data;
                    }
                }
            ],
            "drawCallback": function (settings) {

                /*show pager if only necessary
                console.log(this.fnSettings());*/
                if (Math.ceil((this.fnSettings().fnRecordsDisplay()) / this.fnSettings()._iDisplayLength) > 1) {
                    $('#GenOkpCoDt_paginate').css("display", "block");
                } else {
                    $('#GenOkpCoDt_paginate').css("display", "none");
                }

            }
        });
    } catch (e) {
        $.alert({
            title: _getCookieForLanguage("_informationTitle"),
            content: e,
            type: 'red'
        });
    }
}

