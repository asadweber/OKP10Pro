        private string SortByColumnWithOrder(string order, string orderDir)
        {
            string sortingVal = string.Empty;
            try
            {
                switch (order)
                {
					 case "0":
							 sortingVal = "serial" + " " + orderDir;
							 break;
					 case "1":
							 sortingVal = "sessionid" + " " + orderDir;
							 break;
					 case "2":
							 sortingVal = "userid" + " " + orderDir;
							 break;
					 case "3":
							 sortingVal = "masteruserid" + " " + orderDir;
							 break;
					 case "4":
							 sortingVal = "sessiontoken" + " " + orderDir;
							 break;
					 case "5":
							 sortingVal = "username" + " " + orderDir;
							 break;
					 case "6":
							 sortingVal = "isactive" + " " + orderDir;
							 break;
						 default:
							 sortingVal = "serial" + " " + orderDir;
							 break;
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return sortingVal;
        }

