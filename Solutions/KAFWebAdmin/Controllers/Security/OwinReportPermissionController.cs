        private string SortByColumnWithOrder(string order, string orderDir)
        {
            string sortingVal = string.Empty;
            try
            {
                switch (order)
                {
					 case "0":
							 sortingVal = "reportpersmissionid" + " " + orderDir;
							 break;
					 case "1":
							 sortingVal = "masteruserid" + " " + orderDir;
							 break;
					 case "2":
							 sortingVal = "userid" + " " + orderDir;
							 break;
					 case "3":
							 sortingVal = "reportroleid" + " " + orderDir;
							 break;
					 case "4":
							 sortingVal = "reportid" + " " + orderDir;
							 break;
					 case "5":
							 sortingVal = "isaccessible" + " " + orderDir;
							 break;
					 case "6":
							 sortingVal = "isprintable" + " " + orderDir;
							 break;
						 default:
							 sortingVal = "reportpersmissionid" + " " + orderDir;
							 break;
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return sortingVal;
        }

