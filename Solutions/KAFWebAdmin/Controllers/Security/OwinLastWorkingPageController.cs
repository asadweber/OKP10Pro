        private string SortByColumnWithOrder(string order, string orderDir)
        {
            string sortingVal = string.Empty;
            try
            {
                switch (order)
                {
					 case "0":
							 sortingVal = "lastpageid" + " " + orderDir;
							 break;
					 case "1":
							 sortingVal = "appformid" + " " + orderDir;
							 break;
					 case "2":
							 sortingVal = "userid" + " " + orderDir;
							 break;
					 case "3":
							 sortingVal = "masteruserid" + " " + orderDir;
							 break;
					 case "4":
							 sortingVal = "lastentrydate" + " " + orderDir;
							 break;
						 default:
							 sortingVal = "lastpageid" + " " + orderDir;
							 break;
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return sortingVal;
        }

