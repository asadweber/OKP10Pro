        private string SortByColumnWithOrder(string order, string orderDir)
        {
            string sortingVal = string.Empty;
            try
            {
                switch (order)
                {
					 case "0":
							 sortingVal = "reportroleid" + " " + orderDir;
							 break;
					 case "1":
							 sortingVal = "reportrolename" + " " + orderDir;
							 break;
					 case "2":
							 sortingVal = "description" + " " + orderDir;
							 break;
						 default:
							 sortingVal = "reportroleid" + " " + orderDir;
							 break;
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return sortingVal;
        }

