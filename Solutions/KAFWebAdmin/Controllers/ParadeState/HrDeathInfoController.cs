        private string SortByColumnWithOrder(string order, string orderDir)
        {
            string sortingVal = string.Empty;
            try
            {
                switch (order)
                {
					 case "0":
							 sortingVal = "deathinfoid" + " " + orderDir;
							 break;
				
					 case "1":
							 sortingVal = "hrbasicid" + " " + orderDir;
							 break;
					 case "2":
							 sortingVal = "deathdate" + " " + orderDir;
							 break;
					 case "3":
							 sortingVal = "deathreason" + " " + orderDir;
							 break;
					 case "4":
							 sortingVal = "remarks" + " " + orderDir;
							 break;
						 default:
							 sortingVal = "deathinfoid" + " " + orderDir;
							 break;
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return sortingVal;
        }

