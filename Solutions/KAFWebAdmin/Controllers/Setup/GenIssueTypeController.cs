        private string SortByColumnWithOrder(string order, string orderDir)
        {
            string sortingVal = string.Empty;
            try
            {
                switch (order)
                {
					 case "0":
							 sortingVal = "issuetypeid" + " " + orderDir;
							 break;
					 case "1":
							 sortingVal = "issuetype" + " " + orderDir;
							 break;
					 case "2":
							 sortingVal = "remarks" + " " + orderDir;
							 break;
						 default:
							 sortingVal = "issuetypeid" + " " + orderDir;
							 break;
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return sortingVal;
        }

