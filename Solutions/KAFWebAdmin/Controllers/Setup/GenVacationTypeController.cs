        private string SortByColumnWithOrder(string order, string orderDir)
        {
            string sortingVal = string.Empty;
            try
            {
                switch (order)
                {
					 case "0":
							 sortingVal = "vacationtypeid" + " " + orderDir;
							 break;
					 case "1":
							 sortingVal = "vacationtype" + " " + orderDir;
							 break;
					 case "2":
							 sortingVal = "remarks" + " " + orderDir;
							 break;
						 default:
							 sortingVal = "vacationtypeid" + " " + orderDir;
							 break;
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return sortingVal;
        }

