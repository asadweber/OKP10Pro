        private string SortByColumnWithOrder(string order, string orderDir)
        {
            string sortingVal = string.Empty;
            try
            {
                switch (order)
                {
					 case "0":
							 sortingVal = "bankid" + " " + orderDir;
							 break;
					 case "1":
							 sortingVal = "countryid" + " " + orderDir;
							 break;
					 case "2":
							 sortingVal = "bankname" + " " + orderDir;
							 break;
					 case "3":
							 sortingVal = "remarks" + " " + orderDir;
							 break;
						 default:
							 sortingVal = "bankid" + " " + orderDir;
							 break;
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return sortingVal;
        }

