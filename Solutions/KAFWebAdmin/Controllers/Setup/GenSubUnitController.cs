        private string SortByColumnWithOrder(string order, string orderDir)
        {
            string sortingVal = string.Empty;
            try
            {
                switch (order)
                {
					 case "0":
							 sortingVal = "subunitid" + " " + orderDir;
							 break;
					 case "1":
							 sortingVal = "subunit" + " " + orderDir;
							 break;
					 case "2":
							 sortingVal = "okpid" + " " + orderDir;
							 break;
					 case "3":
							 sortingVal = "remarks" + " " + orderDir;
							 break;
						 default:
							 sortingVal = "subunitid" + " " + orderDir;
							 break;
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return sortingVal;
        }

