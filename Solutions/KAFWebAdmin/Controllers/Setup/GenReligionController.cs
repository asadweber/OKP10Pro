        private string SortByColumnWithOrder(string order, string orderDir)
        {
            string sortingVal = string.Empty;
            try
            {
                switch (order)
                {
					 case "0":
							 sortingVal = "religionid" + " " + orderDir;
							 break;
					 case "1":
							 sortingVal = "religion" + " " + orderDir;
							 break;
					 case "2":
							 sortingVal = "priority" + " " + orderDir;
							 break;
					 case "3":
							 sortingVal = "remarks" + " " + orderDir;
							 break;
						 default:
							 sortingVal = "religionid" + " " + orderDir;
							 break;
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return sortingVal;
        }

