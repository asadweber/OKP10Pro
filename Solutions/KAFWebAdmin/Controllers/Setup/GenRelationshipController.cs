        private string SortByColumnWithOrder(string order, string orderDir)
        {
            string sortingVal = string.Empty;
            try
            {
                switch (order)
                {
					 case "0":
							 sortingVal = "relationshipid" + " " + orderDir;
							 break;
					 case "1":
							 sortingVal = "relationshipname" + " " + orderDir;
							 break;
					 case "2":
							 sortingVal = "priority" + " " + orderDir;
							 break;
					 case "3":
							 sortingVal = "forself" + " " + orderDir;
							 break;
					 case "4":
							 sortingVal = "remarks" + " " + orderDir;
							 break;
						 default:
							 sortingVal = "relationshipid" + " " + orderDir;
							 break;
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
            return sortingVal;
        }

